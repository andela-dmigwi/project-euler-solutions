def linear_congruetial(seed):
    x, l = seed, []
    m = 32
    while(len(l) != m):
        x = ((13 * x) + 19) % m
        l.append(x)
        # x=(a*x+c) mod m

        # m=2**b=2**5=32
        # a=1+4k k=integer a=3*4+1=13
        # c= prime number or an odd number
    if len(set(l)) == len(l):
        return l
    return "Numbers not random enough"

